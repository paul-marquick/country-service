namespace CountryService.DataAccess.ListQuery;

/// <summary>
/// Represents the possible directions for sorting operations.
/// </summary>
/// <remarks>This class provides string constants for specifying the sort direction in operations that require
/// sorting. Use <see cref="Ascending"/> for ascending order and <see cref="Descending"/> for descending
/// order.</remarks>
public static class SortDirection
{
    public const string Ascending = "asc";
    public const string Descending = "desc";
}
